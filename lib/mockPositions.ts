import { Position } from '@/types/position';

// Helper to calculate position values
function calculatePosition(
  side: 'YES' | 'NO',
  shares: number,
  avgPrice: number,
  currentProbability: number
): { currentValue: number; unrealizedPnL: number; unrealizedPnLPercent: number } {
  const costBasis = shares * avgPrice;
  const currentValue = side === 'YES' 
    ? shares * (currentProbability / 100)
    : shares * ((100 - currentProbability) / 100);
  const unrealizedPnL = currentValue - costBasis;
  const unrealizedPnLPercent = (unrealizedPnL / costBasis) * 100;
  
  return { currentValue, unrealizedPnL, unrealizedPnLPercent };
}

